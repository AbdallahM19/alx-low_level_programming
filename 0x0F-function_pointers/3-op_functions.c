#include "3-calc.h"

/**
 * op_add - sum
 * @a: The first num.
 * @b: The second num.
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference
 * @a: The first num.
 * @b: The second num.
 *
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - product
 * @a: The first num.
 * @b: The second num.
 *
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: The first num
 * @b: The second num
 *
 * Return: quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - remainder of the division
 * @a: The first num
 * @b: The second num
 *
 * Return: remainder of the division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
