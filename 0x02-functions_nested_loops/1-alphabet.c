#include"main.h"

/**
 * main - check the code
 *
 * Description:  * print_alphabet - utilizes on the _putchar function to print
 *                 the alphabet a - z
 *
 * Return: Always 0
*/

void print_alphabet(void)
{
int char1;
for (char1 = 'a'; char1 <= 'z'; char1++)
{
_putchar(char1);
}
_putchar('\n');
return (0);
}

